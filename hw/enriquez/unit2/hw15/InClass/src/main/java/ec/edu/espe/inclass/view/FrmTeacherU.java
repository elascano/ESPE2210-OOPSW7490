    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String uri = "mongodb+srv://Cheyli:1234@cheyli.gsvqzfd.mongodb.net/?retryWrites=true&w=majority";
        Object object;
        Teacher teacher;

        try ( MongoClient mongoClient = MongoClients.create(uri)) {
            MongoDatabase database = mongoClient.getDatabase("InClassDB");
            try {
                MongoCollection collectionTeacher = database.getCollection("Teacher");

                String nameToFind;
                nameToFind = String.valueOf(txtName.getText());

                try {
                    Bson projectionFields = Projections.fields(
                            Projections.include("name", "id", "tutorships"),
                            Projections.excludeId());
                    Document teacherFind = (Document) collectionTeacher.find(eq("name", nameToFind))
                            .projection(projectionFields)
                            .first();
                    if (teacherFind == null) {
                        JOptionPane.showMessageDialog(this, "No results found", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
                        emptyFields();
                    } else {
                        object = teacherFind.values().toArray();
                        teacher = DBManager.toTeacher((Object[]) object);

                        txtID.setText(String.valueOf(teacher.getId()));
                        txtTutorship.setText(String.valueOf(teacher.getTutorship()));
                        btnSave.setEnabled(true);
                    }

                } catch (MongoException me) {
                    JOptionPane.showMessageDialog(this, "Unable to insert due to an error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
                    emptyFields();
                }

            } catch (MongoException me) {
                JOptionPane.showMessageDialog(this, "error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String uri = "mongodb+srv://Cheyli:1234@cheyli.gsvqzfd.mongodb.net/?retryWrites=true&w=majority";
        try ( MongoClient mongoClient = MongoClients.create(uri)) {
            MongoDatabase database = mongoClient.getDatabase("InClassDB");
            try {
                MongoCollection collectionTeacher = database.getCollection("Teacher");

                Document teacher = new Document().append("name", String.valueOf(txtName.getText()));
                Bson updates = Updates.combine(
                        Updates.set("id", String.valueOf(txtID.getText())),
                        Updates.set("tutorships", Integer.valueOf(txtTutorship.getText()))
                );
                UpdateOptions options = new UpdateOptions().upsert(true);
                try {
                    UpdateResult result = collectionTeacher.updateOne(teacher, updates, options);
                    JOptionPane.showMessageDialog(this, "Modified document count: " + result.getModifiedCount(), "Update", INFORMATION_MESSAGE);
                    emptyFields();
                } catch (MongoException me) {
                    JOptionPane.showMessageDialog(this, "Unable to update due to an error: " + me, "Error", ERROR_MESSAGE);
                    emptyFields();
                }
            } catch (MongoException me) {
                JOptionPane.showMessageDialog(this, "Unable to insert due to an error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
                emptyFields();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

