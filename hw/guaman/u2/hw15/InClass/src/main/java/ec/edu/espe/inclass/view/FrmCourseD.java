    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String uri = "mongodb+srv://user:1234@cluster0.uz5ahnb.mongodb.net/?retryWrites=true&w=majority";

        try ( MongoClient mongoClient = MongoClients.create(uri)) {
            MongoDatabase database = mongoClient.getDatabase("InClassDB");
            try {
                MongoCollection collectionCourse = database.getCollection("course");

                int nrcToDelete;
                nrcToDelete = Integer.parseInt(txtNrc.getText());

                try {
                    Bson projectionFields = Projections.fields(
                            Projections.include("name", "nrc", "studentNumber"),
                            Projections.excludeId());
                    Document doc = (Document) collectionCourse.find(eq("nrc", nrcToDelete))
                            .projection(projectionFields)
                            .first();
                    if (doc == null) {
                        JOptionPane.showMessageDialog(this, "No results found", "ERROR", JOptionPane.ERROR_MESSAGE);
                    } else {
                        Bson courseDelete = eq("nrc", nrcToDelete);
                        try {
                            DeleteResult result = collectionCourse.deleteOne(courseDelete);
                            JOptionPane.showMessageDialog(this, "Deleted document count: " + result.getDeletedCount(), "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
                        } catch (MongoException me) {
                            JOptionPane.showMessageDialog(this, "Unable to delete due to an error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    emptyFields();
                } catch (MongoException me) {
                    JOptionPane.showMessageDialog(this, "Unable to insert due to an error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
                    emptyFields();
                }

            } catch (MongoException me) {
                JOptionPane.showMessageDialog(this, "error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

