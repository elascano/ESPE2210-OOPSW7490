    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
        // TODO add your handling code here:

        String uri = "mongodb+srv://user:1234@cluster0.uz5ahnb.mongodb.net/?retryWrites=true&w=majority";

        try ( MongoClient mongoClient = MongoClients.create(uri)) {
            MongoDatabase database = mongoClient.getDatabase("InClassDB");
            try {
                MongoCollection collectionCourse = database.getCollection("course");
                Course course;
                
                String name;
                int nrc;
                int studentNumber;

                name = String.valueOf(txtName.getText());
                nrc = Integer.parseInt(txtNrc.getText());
                studentNumber = Integer.parseInt(txtStudentNumber.getText());
                course = new Course(name, nrc, studentNumber);
                
                

                try {
                    InsertOneResult result = collectionCourse.insertOne(new Document()
                            .append("_id", new ObjectId())
                            .append("name", course.getName())
                            .append("nrc", course.getNrc())
                            .append("studentNumber", course.getStudentNumber())
                    );

                    JOptionPane.showMessageDialog(this, "Success! Inserted document id: " + result.getInsertedId(), "OK", JOptionPane.INFORMATION_MESSAGE);
                    emptyFields();
                } catch (MongoException me) {
                    JOptionPane.showMessageDialog(this, "Unable to insert due to an error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
                    emptyFields();
                }

            } catch (MongoException me) {
                JOptionPane.showMessageDialog(this, "error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddCourseActionPerformed

