    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String textName;
        int textId;
        double textPrice;

        textName = txtName.getText();
        textPrice = parseDouble(txtAge.getText());
        textId = parseInt(txtID.getText());
        
        ComputeCaliTaxes sComputeTaxes = ComputeCaliTaxes.getInstance();
        textPrice = sComputeTaxes.computeTaxes(textPrice);

        CRUDinMongoDB sCreateObject = CRUDinMongoDB.getInstance();
        sCreateObject.CreateObjectInDB(textId,textName,textPrice);
        JOptionPane.showMessageDialog(null, "The Price was added sucessfully!");
    }//GEN-LAST:event_btnAddActionPerformed

