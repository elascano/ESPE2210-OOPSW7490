    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DecimalFormat decimal;
        decimal = new DecimalFormat("#.00");
        float price;
        float totalPrice;
        float taxValue;
        String name;
        USTax tax = USTax.getInstance();
        price = Float.parseFloat(txtPrice.getText());
        name = txtProductName.getText();
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        
        totalPrice=CalculatorController.CalculateTotalPrice(price);
        taxValue=CalculatorController.CalculateTaxValue(price);
        
        String[] row = {name,valueOf(price),valueOf(tax.getTax()),valueOf(decimal.format(taxValue)),valueOf(decimal.format(totalPrice))};

        model.addRow(row);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtPriceKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPriceKeyTyped
        int key = evt.getKeyChar();

        boolean numbers = (key >= 48 && key <= 57) || key == 46 ;

        if (!numbers) {
            evt.consume();
        }

        if (txtPrice.getText().trim().length() == 10) {
            evt.consume();
        }
        
        String text;
        char[] textCharacters;
        char character;
        char point;
        point = '.';
        text = txtPrice.getText();
        textCharacters = text.toCharArray();
        
        if(txtPrice.getText().trim().length() == 0 && key == 46){
            evt.consume();
        }
        
        
        if (txtPrice.getText().trim().length() > 0) {
            for (int i = 0; i < txtPrice.getText().trim().length(); i++) {
                
                character = textCharacters[i];
                
                if (Character.compare(character, point) == 0 && key==46) {
                    evt.consume();
                }
            }
        }
        
        
        
    }//GEN-LAST:event_txtPriceKeyTyped

    private void txtTaxValueKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTaxValueKeyTyped
        int key = evt.getKeyChar();

        boolean numbers = (key >= 48 && key <= 57)||key ==46;
        String text;

        if (!numbers) {
            evt.consume();
        }
        
        if (txtTaxValue.getText().trim().length() == 5 && key != 127) {
            evt.consume();
        }

        char[] textCharacters;
        char character;
        char point;
        point = '.';
        text = txtTaxValue.getText();
        textCharacters = text.toCharArray();
        
        if(txtTaxValue.getText().trim().length() == 0 && key == 46){
            evt.consume();
        }
        
        
        if (txtTaxValue.getText().trim().length() > 0) {
            for (int i = 0; i < txtTaxValue.getText().trim().length(); i++) {
                
                character = textCharacters[i];
                
                if (Character.compare(character, point) == 0 && key==46) {
                    evt.consume();
                }
            }
        }
        
        if(txtTaxValue.getText().trim().length() > 2 && key != 46){
            evt.consume();
        }
        
    }//GEN-LAST:event_txtTaxValueKeyTyped

