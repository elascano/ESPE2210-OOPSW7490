    private void btmFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmFindActionPerformed
        Client client;
        client = DBManager.findData(Integer.parseInt(txtID.getText()));
        txtName.setText(client.getName());
        txtNRC.setText(String.valueOf(client.getNrc()));
        txtValue.setText(String.valueOf(client.getValue()));
        txtID.setText(String.valueOf(client.getId()));
        btmUpdate.setEnabled(true);
        btmDelete.setEnabled(true);
        
    }//GEN-LAST:event_btmFindActionPerformed

