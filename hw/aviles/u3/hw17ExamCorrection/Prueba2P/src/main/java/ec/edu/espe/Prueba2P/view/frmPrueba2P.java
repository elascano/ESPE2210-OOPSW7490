    private void btmCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmCreateActionPerformed
        Client client;
        client = new Client(txtName.getText(),Integer.parseInt(txtNRC.getText()),Double.parseDouble(txtValue.getText()),Integer.parseInt(txtID.getText()));
        DBManager.createData(client);
        JOptionPane.showMessageDialog(null, "Cliente creado");
    }//GEN-LAST:event_btmCreateActionPerformed

    private void btmUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmUpdateActionPerformed
        Client client;
        client = new Client(txtName.getText(),Integer.parseInt(txtNRC.getText()),Double.parseDouble(txtValue.getText()),Integer.parseInt(txtID.getText()));
        DBManager.updateData(client);
        JOptionPane.showMessageDialog(null, "Actualizado");
        
    }//GEN-LAST:event_btmUpdateActionPerformed

    private void btmDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmDeleteActionPerformed
        DBManager.deleteData(Integer.parseInt(txtID.getText()));
        txtName.setText(null);
        txtNRC.setText(null);
        txtValue.setText(null);
        txtID.setText(null);
        btmUpdate.setEnabled(false);
        btmUpdate.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Cliente eliminado");
    }//GEN-LAST:event_btmDeleteActionPerformed

