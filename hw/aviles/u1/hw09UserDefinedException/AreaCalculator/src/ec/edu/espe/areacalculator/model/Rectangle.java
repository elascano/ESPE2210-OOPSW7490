package ec.edu.espe.areacalculator.model;

/**
 *
 * @author Daniel Aviles, DeltaTeam, DCCO-ESPE
 */
public class Rectangle {
    private float longSide;
    private float sortSide;
    private float area;

    public float getLongSide() {
        return longSide;
    }

    public void setLongSide(float longSide) {
        this.longSide = longSide;
    }

    public float getSortSide() {
        return sortSide;
    }

    public void setSortSide(float sortSide) {
        this.sortSide = sortSide;
    }

    public float getArea() {
        return area;
    }

    public void setArea(float area) {
        this.area = area;
    }

   

}
