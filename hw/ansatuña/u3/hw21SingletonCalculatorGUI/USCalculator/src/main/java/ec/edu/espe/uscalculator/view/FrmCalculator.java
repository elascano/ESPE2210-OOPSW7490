private float selectedTaxPercentage(String ivaPercentage) {
        switch (ivaPercentage) {
            case ("---") -> {
                return 0.00F;
            }
            case ("14%") -> {
                return 0.14F;
            }
            case ("12%") -> {
                return 0.12F;
            }
            case ("8%") -> {
                return 0.08F;
            }
            case ("4.85%") -> {
                return 0.0485F;
            }
            default -> {
                return 0.00F;
            }
        }
    }
    private void txtPriceKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPriceKeyTyped
        char c = evt.getKeyChar();

        if ((Character.isDigit(c)) || (Character.isWhitespace(c)) || (Character.isISOControl(c))) {
            txtPrice.setEditable(true);

        } else {
            txtPrice.setEditable(false);
            JOptionPane.showMessageDialog(this, c + " is not accepted here", "Warning on input data", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtPriceKeyTyped

    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        float total;
        float price;
        float iva;
        String percent;
        Scanner console = new Scanner(System.in);

        USTax tax = USTax.getInstance();
        price = Float.parseFloat(txtPrice.getText());
        percent = String.valueOf(cbIva.getSelectedItem());
        iva = selectedTaxPercentage(percent);
        tax.modifyTax(iva);
        total = tax.salesTotal(price);
        
        String[] Data = new String[5];
        Data[0] = Integer.toString(counter);
        Data[1] = txtProduct.getText();
        Data[2] = Float.toString(price);
        Data[3] = String.format("%.2f", tax.getTaxValue());
        Data[4] = Float.toString(total);
        model.addRow(Data);
        emptySpaces();
        counter++;
    }//GEN-LAST:event_btnCalculateActionPerformed
    private void emptySpaces() {
        txtPrice.setText("");
        txtProduct.setText("");
        cbIva.setSelectedItem("---");
    }
