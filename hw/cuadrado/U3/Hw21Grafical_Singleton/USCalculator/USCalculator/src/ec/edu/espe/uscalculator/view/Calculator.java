    private void btmCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmCalculateActionPerformed
        float price;
        float total;
        float iva;
        String percent;

        Scanner console = new Scanner(System.in);
        USTax tax = USTax.getInstance();
        price = Float.parseFloat(String.valueOf(txtProduct.getText()));

        percent = String.valueOf(cbxTax.getSelectedItem());
        iva = selectStateTax(percent);

        tax.modifyTax(iva);
        total = tax.salesTotal(price);

        txtPrice.setText("Price: " + price + "$");
        txtTax.setText("Tax: " + tax.getTaxValue() + "%");
        txtTo.setText("Total price: " + total + "$");
        txtProduct.setText("");

    }//GEN-LAST:event_btmCalculateActionPerformed

