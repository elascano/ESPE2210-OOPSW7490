    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        float price;
        float total;
        float iva;
        String percent;
        DecimalFormat df = new DecimalFormat("#.##");
        Scanner console = new Scanner(System.in);
        USTax tax = USTax.getInstance();
        price = Float.parseFloat(String.valueOf(txtPrice.getText()));

        percent = String.valueOf(cmbTax.getSelectedItem());
        iva = selectStateTax(percent);

        tax.modifyTax(iva);
        total = tax.salesTotal(price);

        lbPrice.setText(price + "$");
        lbTax.setText(df.format(tax.getTaxValue()) + "%");
        lbTprice.setText(total + "$");
        txtPrice.setText("");
    }//GEN-LAST:event_btnAcceptActionPerformed

