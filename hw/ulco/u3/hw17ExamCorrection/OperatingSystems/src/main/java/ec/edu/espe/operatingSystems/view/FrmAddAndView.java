    private void View() {
        Gson gson = new Gson();
        OperatingSystem operatingSystem = new OperatingSystem();
        ArrayList<OperatingSystem> operatingSystems = new ArrayList<OperatingSystem>();
        String uri = "mongodb+srv://oop:oop@cluster0.hvgjsvt.mongodb.net/?retryWrites=true&w=majority";
        MongoClient mongoClient = MongoClients.create(uri);
        MongoDatabase database = mongoClient.getDatabase("OperatingSystems");
        String[] titles = {"Id", "Name", "Year"};
        String[] OperatingSystemString = new String[3];
        DefaultTableModel tableOS = new DefaultTableModel(null, titles);
        tbView.setModel(tableOS);
        
        OperatingSystemController.View(operatingSystems, database, "OperatingSystem");
        for (int i = 0; i < operatingSystems.size(); i++) {
            OperatingSystemString[0] = "" + operatingSystems.get(i).getId()+ "";
            OperatingSystemString[1] = "" + operatingSystems.get(i).getName()+ "";
            OperatingSystemString[2] = "" + operatingSystems.get(i).getYear()+ "";
            
            tableOS.addRow(OperatingSystemString);
        }
        tbView.setModel(tableOS);
        tbView.setDefaultEditor(Object.class, null);
    }

    

