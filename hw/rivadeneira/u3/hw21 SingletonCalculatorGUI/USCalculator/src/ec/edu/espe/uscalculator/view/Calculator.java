    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        float price;
        float total;
        float iva;
        String percent;

        USTax tax = USTax.getInstance();
        price = Float.parseFloat(String.valueOf(txtPriceProduct.getText()));

        percent = String.valueOf(cbxTax.getSelectedItem());
        iva = selectStateTax(percent);

        tax.modifyTax(iva);
        total = tax.salesTotal(price);

        txtPrice.setText("Price: " + price + "$");
        txtTax.setText("Tax: " + tax.getTaxValue() + "%");
        txtTotal.setText("Total price: " + total + "$");
        txtPriceProduct.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

