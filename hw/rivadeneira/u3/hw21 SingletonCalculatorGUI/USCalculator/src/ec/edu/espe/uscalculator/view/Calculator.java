    private float selectStateTax(String percent) {
        switch (percent) {
            case("4.85%")->{return 4.85F;}
            case("14%")->{return 14.0F;}
            case("8%")->{return 8.0F;}
            case("12%")->{return 12.0F;}
            default -> {return 0.0F;}
        }           
    }
