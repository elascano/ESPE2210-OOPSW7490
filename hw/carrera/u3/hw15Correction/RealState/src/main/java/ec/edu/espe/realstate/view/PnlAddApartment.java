    private void initPnlStartPage(){
        apartmentsCollection = ApartmentController.createConnectionToCollection();
        PnlStartPage pnlStartPage = new PnlStartPage();
        pnlStartPage.setLocation(0, 0);
        pnlStartPage.setSize(406,536);
        pnlWindow.removeAll();
        pnlWindow.add(pnlStartPage,pnlStartPage);
        pnlWindow.revalidate();
        pnlWindow.repaint();
    }
    
    private boolean addApartment(){
        Apartment apartmentToAdd = createApartmentToAdd();
        changeIdLabel();
        return ApartmentController.insertApartment(apartmentsCollection,apartmentToAdd);
    }
    
    private void changeIdLabel(){
        String newId = String.valueOf(ApartmentController.assigIdToApartment(apartmentsCollection));
        lblId.setText(newId);
    }
    private Apartment createApartmentToAdd(){
        float cost = Float.parseFloat(String.valueOf(spnrCost.getValue()));
        int floor = Integer.valueOf(sldrFloor.getValue());
        Apartment apartmentToAdd;
        apartmentToAdd = new Apartment(0, floor, cost);
        return apartmentToAdd;
    }
    private void cleanFields(){
        changeIdLabel();
        spnrCost.setValue(0);
        sldrFloor.setValue(5);
    }
    
