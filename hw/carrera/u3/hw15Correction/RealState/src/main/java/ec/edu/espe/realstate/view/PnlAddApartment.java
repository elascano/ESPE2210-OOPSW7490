    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       int decision = JOptionPane.showConfirmDialog(this,"Are you sure you want to save this apartment?", "Add Apartment", JOptionPane.YES_NO_OPTION);
            switch(decision){
                case 0 -> {
                    if(addApartment()){
                        JOptionPane.showMessageDialog(this, "Your apartment has been successfully saved!", "Added successfully", JOptionPane.INFORMATION_MESSAGE);
                        cleanFields();
                    } else{
                       JOptionPane.showMessageDialog(this, "Something went wrong. Failed to save this apartment", "Saved failed", JOptionPane.ERROR_MESSAGE); 
                    }        
                }

            }
    }//GEN-LAST:event_btnAddActionPerformed


