    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String []info = new String[3];
        info[0] = txtIdOrder.getText();
        info[1] = txtNameClient.getText();
        info[2] = txtEmailClient.getText();
        modelo.addRow(info);
        
        txtIdOrder.setText("");
        txtNameClient.setText("");
        txtEmailClient.setText("");
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblRegistrationClient.getSelectedRow();
        if(row >= 0){
            modelo.removeRow(row);
        }else{
            JOptionPane.showMessageDialog(null, "Select the row");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = Integer.parseInt(txtRow.getText());
        int column = Integer.parseInt(txtColumn.getText());
        modelo.setValueAt(txtNewData.getText(), row, column);
    }//GEN-LAST:event_btnUpdateActionPerformed

