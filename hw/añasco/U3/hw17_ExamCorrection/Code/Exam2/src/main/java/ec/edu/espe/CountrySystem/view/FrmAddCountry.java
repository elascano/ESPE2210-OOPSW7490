    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        DataPersistance dataPersistance = null;
        String uri = "mongodb+srv://programmer:database@oop.1fqy266.mongodb.net/?retryWrites=true&w=majority"; 
        Country country = new Country(txtName.getText(), 
                Integer.parseInt(txtPopulation.getText()), txtCurrentPresident.getText());
        String json = dataPersistance.toJson(country);
        
        DbManager dbManager = new DbManager();
        dbManager.connect(uri, "Countries");
        dbManager.insert("Country", json);
    }//GEN-LAST:event_btnInsertActionPerformed

