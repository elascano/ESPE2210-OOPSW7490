    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        String productName;
        float price;
        String state;
        float stateTaxPercentage;
        float taxPerProduct;
        float totalPrice;
        
        productName = txtProductName.getText();
        price = Float.parseFloat(txtPrice.getText());
        state = cmbState.getSelectedItem().toString();
        stateTaxPercentage = getStateTax(state);
        taxPerProduct = calculateTaxPerProduct(price, stateTaxPercentage);
        totalPrice = salesTotal(price, taxPerProduct);
        
        Vector<String> vector = new Vector<>();
        vector.add(productName);
        vector.add(String.valueOf(price));
        vector.add(state);
        vector.add(String.valueOf(stateTaxPercentage));
        vector.add(String.valueOf(taxPerProduct));
        vector.add(String.valueOf(totalPrice));
        
        DefaultTableModel model = (DefaultTableModel)tblTax.getModel();
        model.addRow(vector);
    }//GEN-LAST:event_btnCalculateActionPerformed

