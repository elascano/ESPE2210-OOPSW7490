    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        int option = JOptionPane.showConfirmDialog(this, "Do you want to continue?");
        if (option == 0) {
            if (!txtId.getText().isEmpty() && cmbModel.getSelectedItem() != null && spnCapacity.getValue() != null) {
                ship = new CruiseShip(Integer.parseInt(txtId.getText()), cmbModel.getSelectedItem().toString(),
                         Long.parseLong(spnCapacity.getValue().toString()));
                ships.add(ship);
                dataBaseManager = CruiseShipController.connectToDatabase(uri, databaseName, dataBaseManager);
                CruiseShipController.create(ship, dataBaseManager.getDatabase(), collectionName);
                JOptionPane.showMessageDialog(null, "The ship has been created");
            } else {
                JOptionPane.showMessageDialog(null, "Error, some fields are empty ");
            }
        }
    }//GEN-LAST:event_btnCreateActionPerformed

