    private void addToTable() {

        String[] travelsString = new String[2];
        travelsString[0] = txtNameOfProduct.getText();
        travelsString[1] = txtBaseValue.getText();

        tableOfProducts.addRow(travelsString);

        jtblProducts.setModel(tableOfProducts);
        jtblProducts.setDefaultEditor(Object.class, null);
    }

