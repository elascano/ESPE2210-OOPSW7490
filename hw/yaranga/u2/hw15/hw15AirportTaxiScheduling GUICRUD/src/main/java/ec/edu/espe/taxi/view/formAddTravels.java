    private void txtAddressKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAddressKeyTyped
        // TODO validate if it not a char
        char c = evt.getKeyChar();

        if (txtAddress.getText() != null) {
            btnAccept.setEnabled(true);
        }

        if (!(Character.isAlphabetic(c)) && (c != KeyEvent.VK_BACK_SPACE) && (c != KeyEvent.VK_DELETE) && (c != KeyEvent.VK_SPACE) && !(Character.isDigit(c)) && !(c != KeyEvent.VK_ENTER)) {
            JOptionPane.showMessageDialog(this, c + "is not accepted for Address", "Warning on input data", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtAddressKeyTyped

