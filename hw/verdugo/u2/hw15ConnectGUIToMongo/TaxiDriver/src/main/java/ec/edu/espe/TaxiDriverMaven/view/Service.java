    void Delete(){
        int row = tblData.getSelectedRow();
        dtm.removeRow(row);
    }
    
    void Update(){
        int row = tblData.getSelectedRow();
        dtm.setValueAt(txtID.getText(), row, 0);
        dtm.setValueAt(txtName.getText(), row, 1);
        dtm.setValueAt(txtPlatesNumber.getText(), row, 2);
        dtm.setValueAt(txtDateTravels.getText(), row, 3);
        dtm.setValueAt(txtProfits.getText(), row, 4);
    }
    
    
