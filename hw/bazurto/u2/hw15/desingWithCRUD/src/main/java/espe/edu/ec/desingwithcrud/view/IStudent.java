    private void addToTable() {
        Student student = new Student();
        String dateOfBirth = "";
        student.setId(generateID());
        student.setName(tfName.getText());
        student.setMail(tfEmail.getText());
        dateOfBirth = cbDay.getSelectedItem().toString() + "/" + cbMonth.getSelectedItem().toString() + "/" + cbYear.getSelectedItem().toString();
        student.setDateOfBirth(dateOfBirth);
        ConnectionMongo.insertStudent(student);
        Object []dataStudent = new Object[4];
        dataStudent[0] = student.getId();
        dataStudent[1] = student.getName();
        dataStudent[2] = student.getMail();
        dataStudent[3] = student.getDateOfBirth();
        model.addRow(dataStudent);
        
        setDefaultOptions();
    }

    private void setDefaultOptions() {
        tfName.setText("");
        tfEmail.setText("");
        cbDay.setSelectedIndex(0);
        cbMonth.setSelectedIndex(0);
        cbYear.setSelectedIndex(0);
    }

