    private void bt2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt2ActionPerformed
        if(txt0.getText().isEmpty()){
            JOptionPane.showMessageDialog( null,"Error, Id field is empty");
        }else{
            int takeId = Integer.valueOf(txt0.getText());
            Traveler traveler = new Traveler();
            traveler = dbController.findDocumentdb(traveler, takeId);
            txt1.setText(traveler.getName());
            txt2.setText(Long.toString(traveler.getPhoneNumber()));
            if(traveler.getMail().contains("@gmail.com")==true){
                txt3.setText(traveler.getMail().replaceAll("@gmail.com", ""));
            }else if(traveler.getMail().contains("@hotmail.com")){
                txt3.setText(traveler.getMail().replaceAll("@hotmail.com", ""));
            }else{
                txt3.setText("");
            }
            txt4.setText(Float.toString(traveler.getOutstandingBalance()));
            if(jRadioButton5.isSelected()==true && traveler.getPhoneNumber()!=0){
                txt0.setEditable(false);
                bt2.setEnabled(false);
                bt3.setEnabled(true);
            }if(jRadioButton6.isSelected()==true && traveler.getPhoneNumber()!=0){
                txt0.setEditable(false);
                bt2.setEnabled(false);
                bt4.setEnabled(true);
                txt1.setEnabled(true);
                txt2.setEnabled(true);
                txt3.setEnabled(true);
                txt4.setEnabled(true);
                rb1.setEnabled(true);
                rb2.setEnabled(true);
            }
        }
        
        
    }//GEN-LAST:event_bt2ActionPerformed

    private void bt1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt1ActionPerformed
        if(txt0.getText().isEmpty() || txt1.getText().isEmpty() || txt2.getText().isEmpty() || txt3.getText().isEmpty() || txt4.getText().isEmpty()){
            JOptionPane.showMessageDialog( null,"Error, some fields are empty ");
        }else{
            
            Traveler traveler = new Traveler();
            traveler.setId(Integer.valueOf(txt0.getText()));
            traveler.setName(txt1.getText());
            traveler.setPhoneNumber(Integer.valueOf(txt2.getText()));
            if(rb1.isSelected()==true){
                String gmail = "@gmail.com";
                traveler.setMail(txt3.getText()+gmail);
            }else{
                String gmail = "@hotmail.com";
                traveler.setMail(txt3.getText()+gmail);
            }
            traveler.setOutstandingBalance(Float.valueOf(txt4.getText()));
            dbController.uploadDocumentMdb(traveler);
            cleanPanel();
            JOptionPane.showMessageDialog( null,"Upload successfully");
            
        }
    }//GEN-LAST:event_bt1ActionPerformed

