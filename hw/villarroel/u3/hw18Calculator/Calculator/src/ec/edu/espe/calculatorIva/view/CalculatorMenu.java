          private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
                    if (txtProduct.getText().isEmpty() || txtTax.getText().isEmpty()) {
                              JOptionPane.showMessageDialog(null, "Error, some fields are empty");
                    } else {
                              DecimalFormat formatNumber = new DecimalFormat("0.00");
                              double price = Float.parseFloat(txtProduct.getText());
                              double tax = Float.parseFloat(txtTax.getText());
                              price = price + ((price * tax) / 100);
                              JOptionPane.showMessageDialog(null, "The price of the product with IVA is: " + formatNumber.format(price));
                    }
          }//GEN-LAST:event_jToggleButton1ActionPerformed

