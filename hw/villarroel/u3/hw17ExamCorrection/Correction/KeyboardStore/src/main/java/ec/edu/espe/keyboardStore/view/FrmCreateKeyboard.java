    private void crateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crateButtonActionPerformed
        if (idTextField.getText().isEmpty() || tradeMarkTF.getText().isEmpty() || priceTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Error, some fields are empty ");
        } else {
            Keyboard keyboard = new Keyboard();
            keyboard.setId(Integer.parseInt(idTextField.getText()));
            keyboard.setKeyboardType(keyboardType.getSelectedItem().toString());
            keyboard.setTradeMark(tradeMarkTF.getText());
            keyboard.setPrice(Float.parseFloat(priceTF.getText()));
            keyboard = KeyboardController.findKeyboardMdb(keyboard, Integer.parseInt(idTextField.getText()));
            if (keyboard.getPrice() != 0) {
                KeyboardController.createKeyboardMdb(keyboard);
                JOptionPane.showMessageDialog(null, "Keyboard created Sucessfully");
            } else {
                JOptionPane.showMessageDialog(null, "Error, id already registered");
            }
        }
    }//GEN-LAST:event_crateButtonActionPerformed

    private void idTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idTextFieldKeyTyped
        int validNumber = evt.getKeyChar();
        boolean number = validNumber >= 48 && validNumber <= 57;
        if (!number) {
            evt.consume();
        }
        if (idTextField.getText().length() >= 5) {
            evt.consume();
        }
    }//GEN-LAST:event_idTextFieldKeyTyped

    private void priceTFKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_priceTFKeyTyped
        int validNumber = evt.getKeyChar();
        boolean number = (validNumber >= 48 && validNumber <= 57) || validNumber == 46;
        if (!number) {
            evt.consume();
        }
        if (priceTF.getText().length() >= 10) {
            evt.consume();
        }
    }//GEN-LAST:event_priceTFKeyTyped

