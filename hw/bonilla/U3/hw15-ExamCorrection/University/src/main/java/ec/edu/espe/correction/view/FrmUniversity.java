    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        name = String.valueOf(txtName.getText());
        id = Integer.parseInt(txtId.getText());
        ranking = Integer.parseInt(txtRanking.getText());
        university = new University(name, id, ranking);
        mongoDBManager = new MongoDBManager();
        mongoDBManager.conectToMongoDb(uri);
        if (mongoDBManager.verifyRepertorieId("University", university)==true) 
        {
            JOptionPane.showMessageDialog(this, "Id already exist", "Warning on input data", JOptionPane.WARNING_MESSAGE);
            emptyText();

        } else 
        {
            UniversityController.insertUniverstyDocument(university);
            mongoDBManager.insertDocumentDataBase("University", UniversityController.insertUniverstyDocument(university));
            JOptionPane.showMessageDialog(null, "University inserted successfully");
            
        }

    }//GEN-LAST:event_btnInsertActionPerformed

