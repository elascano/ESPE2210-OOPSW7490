    public void validateEmptyData()
    {
        if(txtId.getText().isEmpty())
        {
            lblValidateId.setText("obligatory data");
        }
        else
        {
            lblValidateId.setText("");
        }
        if(txtName.getText().isEmpty())
        {
            lblValidateName.setText("obligatory data");
        }
        else
        {
            lblValidateName.setText("");
        }
        if(txtRanking.getText().isEmpty())
        {
            lblValidateRanking.setText("obligatory data");
        }
        else
        {
            lblValidateRanking.setText("");
        }
        if(txtId.getText().isEmpty()||txtName.getText().isEmpty()||txtRanking.getText().isEmpty())
        {
            btnInsert.setEnabled(false);
        }
        else
        {
            btnInsert.setEnabled(true);
        }
    }

