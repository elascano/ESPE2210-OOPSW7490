    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
        if (txtPrice.getText() != null && !"".equals(txtPrice.getText())) {
            disableBtnCalc();
            double price = Double.parseDouble(txtPrice.getText());
            String total = String.valueOf(computeProductPrice(price));
            lbTotalPrice.setText(total);
        }else{
            JOptionPane.showMessageDialog(this, "Please, insert a price to calculate", "EMPTY PRICE", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcActionPerformed

    private void txtPriceKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPriceKeyTyped
        // TODO add your handling code here:

        char validate = evt.getKeyChar();
        disableBtnCalc();
        if ((validate < '0' || validate > '9') && txtPrice.getText().contains(".") && (validate != (char) KeyEvent.VK_BACK_SPACE)) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Insert just decimal numbers", "Student ID", JOptionPane.WARNING_MESSAGE);
        } else if ((validate < '0' || validate > '9') && (validate != '.') && (validate != (char) KeyEvent.VK_BACK_SPACE)) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Insert just decimal numbers", "Student ID", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtPriceKeyTyped

