    private void cmbElementItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbElementItemStateChanged
        UtahTax utahTax = UtahTax.getInstance();
        String element;
        float price;
        element = cmbElement.getSelectedItem().toString();

        switch (element) {
            case "Computer":
                price = 599.99F;
                break;
            case "TV":
                price = 459.99F;
                break;
            case "Phone":
                price = 278.44F;
                break;

            default:
                price = 0;
        }

        txtPrice.setText("Price: $" + price);
        txtIva.setText("IVA: " + utahTax.getIvaPersentage() + "% - Tax: $" + price * utahTax.getIvaPersentage() / 100);
        txtTotalPrice.setText("Total price: $" + utahTax.salesTotal(price));
    }//GEN-LAST:event_cmbElementItemStateChanged

