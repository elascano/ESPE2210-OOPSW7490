    private void btnAddStudent1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddStudent1MouseClicked
        
        boolean isDataOk = true;
        
        lblWrongName.setForeground(Color.white);
        lblWrongId.setForeground(Color.white);
        
        if (!StudentController.validateName(txtName.getText())) {            
            lblWrongName.setForeground(new Color(204,102,0));
            isDataOk = false;
        }
        
        if (StudentController.findId(frmStudents.teacher.getCourses().get(0).getStudents(), txtId.getText()) != -1) {
            lblWrongId.setText("(*) The ESPE ID is already registered");
            lblWrongId.setForeground(new Color(204,102,0));
            isDataOk = false;
        }
        
        if(!StudentController.validateId(txtId.getText())) {
            lblWrongId.setText("(*) The ID format must be L########");
            lblWrongId.setForeground(new Color(204,102,0));            
            isDataOk = false;
        }                
        
        if (isDataOk) {
            frmStudents.addStudentGui(txtName.getText().toUpperCase(), txtId.getText());
        }
                
    }//GEN-LAST:event_btnAddStudent1MouseClicked

