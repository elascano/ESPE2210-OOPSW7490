    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(this, "Do you want to continue?");
        if (option == 0) {
            HardDrives product = new HardDrives(txtIdDeleteProduct.getText(), "", "");
            Manager.deleteProduct(product);
            JOptionPane.showMessageDialog(this, "Product Delete");
            txtIdDeleteProduct.setText("");
        } else if (option == 1) {
            JOptionPane.showMessageDialog(this, "Operation Cancelled");
        } else if (option == 2) {
            HardDrivesSystem backBtm = new HardDrivesSystem();
            backBtm.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

