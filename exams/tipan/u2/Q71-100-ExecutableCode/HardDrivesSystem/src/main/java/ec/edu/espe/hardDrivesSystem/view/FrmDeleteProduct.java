    private void txtIdDeleteProductKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdDeleteProductKeyTyped
        // TODO add your handling code here:
        char d = evt.getKeyChar();

        if ((Character.isDigit(d)) || (Character.isISOControl(d))) {
            txtIdDeleteProduct.setEditable(true);
        } else {
            txtIdDeleteProduct.setEditable(false);
            JOptionPane.showMessageDialog(this, d + " is not accepted here", "Warning on input data", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtIdDeleteProductKeyTyped

