    private void txtIdKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdKeyTyped
        // TODO add your handling code here:
         char d = evt.getKeyChar();

        if ((Character.isDigit(d))||(Character.isISOControl(d))) {
            txtId.setEditable(true);
        }
        else{
            txtId.setEditable(false);
            JOptionPane.showMessageDialog(this,d + " is not accepted here","Warning on input data",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtIdKeyTyped

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        HardDrives product = new HardDrives(txtId.getText(), "", "");
        product = Manager.findProduct(product);
         String []Datos = new String [4];
         Datos[0] = product.getId();
         Datos[1]= product.getInterfac();
         Datos[2] = product.getStorage();
         txtId.setText("");
         model.addRow(Datos);
    }//GEN-LAST:event_btnSearchActionPerformed

