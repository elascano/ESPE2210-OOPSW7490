    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String []Datos = new String [4];
        
        int reEnterId = Manager.dniValidation(txtId.getText());

        if(reEnterId == 0){
            JOptionPane.showMessageDialog(this,"ID invalid! Must have 10 digits",
                    "WARNING",JOptionPane.WARNING_MESSAGE);
            txtId.setText("");
            
        }else if(reEnterId == 1 && !"-Selection-".equals(cmbStorage.getSelectedItem().toString())){
            Datos[0] = txtId.getText();
            Datos[1]= txtInterfac.getText();
            Datos[2] = cmbStorage.getSelectedItem().toString();
            txtId.setText("");
            txtInterfac.setText("");
            cmbStorage.setSelectedIndex(0);
            model.addRow(Datos);
            
           HardDrives product = new HardDrives(Datos[0], Datos[2], Datos[1]);
           Manager.insertProduct(product);
        }

    }//GEN-LAST:event_btnAddActionPerformed

