    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        StrategyMongoConection strategyMongoConection = new StrategyMongoConection();
        String collectionName = "arrayWalther";
        strategyMongoConection = StrategyMongoConection.connectToDatabase(strategyMongoConection);
        SortingContext sortingContext = new SortingContext();
        Strategy strategy = new Strategy();
        String[] numbers = txtOrder.getText().split(",");
        int[] dataToSort = new int[numbers.length];
        for (int i = 0; i < numbers.length; i++) {
            dataToSort[i] = Integer.parseInt(numbers[i].trim());
        }

        strategy.setUnsorted(dataToSort);
        strategy.setSize(dataToSort.length);
        strategy = sortingContext.sort(strategy);

        StrategyMongoConection.createOne(strategy, strategyMongoConection.getDatabase(), collectionName);
        txtOrder.setText(Arrays.toString(strategy.getSorted()));
        txtSort.setText(strategy.getSortAlgorithm());        
    }//GEN-LAST:event_btnSortActionPerformed

    private void txtNumberKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumberKeyTyped
        char character = evt.getKeyChar();

        if (!Character.isDigit(character) && character != 44 && (character != KeyEvent.VK_BACK_SPACE) && (character != KeyEvent.VK_DELETE) && (character != KeyEvent.VK_SPACE) && (character != KeyEvent.VK_ENTER)) {
            JOptionPane.showMessageDialog(this, character + " Not accepted", "Warning! on the data ingress", JOptionPane.WARNING_MESSAGE);
            evt.consume();
        }
    }//GEN-LAST:event_txtNumberKeyTyped

