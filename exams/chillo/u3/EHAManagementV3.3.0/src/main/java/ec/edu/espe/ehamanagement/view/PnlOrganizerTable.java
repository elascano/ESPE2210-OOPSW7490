    private void initPnlOrganizerUpdateAndDeleteMaterial(int id){
        PnlOrganizerUpdateAndDeleteMaterial.setIdToUpdate(id);
        PnlOrganizerUpdateAndDeleteMaterial pnlUpdateAndDeleteMaterial = new PnlOrganizerUpdateAndDeleteMaterial(materialsCollection);
        pnlUpdateAndDeleteMaterial.setSize(714,523);
        pnlUpdateAndDeleteMaterial.setLocation(0,0);
        pnlContent.removeAll();
        pnlContent.add(pnlUpdateAndDeleteMaterial,pnlUpdateAndDeleteMaterial );
        pnlContent.revalidate();
        pnlContent.repaint(); 
    }
    
    private void refreshTable(){

        if(!readedMaterials.isEmpty()){
            createRow(readedMaterials);
        }else{
            JOptionPane.showMessageDialog(this, "ThereÂ´s no materials in your organizer", "Empty Organizer", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void createRow(ArrayList <Material> materials){
        for(Material material: materials){
                String[] information = new String[3];
                information[0]= String.valueOf(material.getId());
                information[1] = material.getName();
                information [2] = String.valueOf(material.getUnitCost());
                tableModel.addRow(information);
            }
    } 
    
    private void validateFields() {
        if (!txtSearch.getText().isEmpty() && !txtSearch.getText().equals("Search...")) {
            btnSearch.setEnabled(true);
        } else {
            btnSearch.setEnabled(false);
        }
    }
