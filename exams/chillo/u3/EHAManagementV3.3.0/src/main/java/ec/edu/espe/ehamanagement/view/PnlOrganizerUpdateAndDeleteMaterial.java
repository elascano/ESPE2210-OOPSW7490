    private boolean updateMaterial() throws ParseException{
        Material materialToUpdate = createMaterialToUpdate();
        lblUnitCost.setText(String.valueOf(materialToUpdate.getUnitCost()));
        return MaterialsOrganizer.updateMaterial(materialsCollection,materialToUpdate, this);
    }
    
    private Material createMaterialToUpdate() throws ParseException{
        String name = txtMaterialName.getText();
        int generalQuantity = Integer.valueOf(String.valueOf(spnrQuantity.getValue()));
        float generalCost = Float.valueOf(txtGeneralCost.getText());
        float unitCost = Float.valueOf(lblUnitCost.getText());
        Material materialToAdd = new Material(idToUpdate, name, generalQuantity, generalCost, unitCost);
        CostsCalculator calculator = new CostsCalculator();
        float unitCostCalculated = calculator.computeMaterialUnitCost(materialToAdd);
        materialToAdd.setUnitCost(unitCostCalculated);
        return materialToAdd;
    }
    


    public void validateFields() {
        if (txtMaterialName.getText().isEmpty() || (txtGeneralCost.getText().isEmpty()
                || txtGeneralCost.getText().equals("0.00") || txtMaterialName.getText().equals("Name"))) {
            btnSave.setEnabled(false);
        } else {
            btnSave.setEnabled(true);
        }
    }
    
    public void openFields(boolean active) {
        txtMaterialName.setEditable(active);
        txtGeneralCost.setEditable(active);
        spnrQuantity.setEnabled(active);
        btnSave.setEnabled(active);

    }

    private void writeFields(){
        String name = (String) MaterialsOrganizer.getValueFromMaterial(materialsCollection, "id",idToUpdate,"name");
        double generalQuantity = Double.valueOf(String.valueOf(MaterialsOrganizer.getValueFromMaterial(materialsCollection, "id",idToUpdate,"generalQuantity")));
        String generalCost = String.valueOf(MaterialsOrganizer.getValueFromMaterial(materialsCollection,"id",idToUpdate,"generalCost"));
        String unitCost = String.valueOf(MaterialsOrganizer.getValueFromMaterial(materialsCollection,"id",idToUpdate,"unitCost"));
        txtMaterialName.setText(name);
        spnrQuantity.setValue((int)generalQuantity);
        txtGeneralCost.setText(generalCost);
        lblUnitCost.setText(unitCost);
    }
