    private void btmFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmFindActionPerformed
        Keyboard keyboard;
        keyboard = DBManager.findData(Integer.parseInt(txtId.getText()));
        txtId.setText(String.valueOf(keyboard.getId()));
        txtLong.setText(String.valueOf(keyboard.getLongInCm()));
        txtBroad.setText(String.valueOf(keyboard.getWidthInCm()));
        btmUpdate.setEnabled(true);
        btmDelete.setEnabled(true);
    }//GEN-LAST:event_btmFindActionPerformed

    private void btmCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmCreateActionPerformed
        Keyboard keyboard;
        keyboard = new Keyboard(Integer.parseInt(txtId.getText()),Double.parseDouble(txtLong.getText()),Double.parseDouble(txtBroad.getText()));
        DBManager.createData(keyboard);
        JOptionPane.showMessageDialog(null, "KeyBoard Created");
    }//GEN-LAST:event_btmCreateActionPerformed

    private void btmUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmUpdateActionPerformed
        Keyboard keyboard;
        keyboard = new Keyboard(Integer.parseInt(txtId.getText()),Double.parseDouble(txtLong.getText()),Double.parseDouble(txtBroad.getText()));
        DBManager.updateData(keyboard);
        JOptionPane.showMessageDialog(null, "Keyboard updated");
    }//GEN-LAST:event_btmUpdateActionPerformed

    private void btmDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmDeleteActionPerformed
        DBManager.deleteData(Integer.parseInt(txtId.getText()));
        txtId.setText(null);
        txtLong.setText(null);
        txtBroad.setText(null);
        btmUpdate.setEnabled(false);
        btmUpdate.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Keyboard eliminated");
    }//GEN-LAST:event_btmDeleteActionPerformed

