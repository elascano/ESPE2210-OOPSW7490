    private void mostTable() {

        String[] info = new String[3];

        model.addColumn("Id");
        model.addColumn("Month");
        model.addColumn("Payment");
        this.table.setModel(model);

        for (int i = 0; i < FrmNewPayment.paymentList.size(); i++) {

            info[0] = Long.toString(FrmNewPayment.paymentList.get(i).getIdPayment());
            info[1] = FrmNewPayment.paymentList.get(i).getMonth();
            info[2] = Float.toString(FrmNewPayment.paymentList.get(i).getPayment());

            model.addRow(info);

        }

    }

