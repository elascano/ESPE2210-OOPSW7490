    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        boolean flag = false;
        int cityID = Integer.parseInt(txtID.getText());
        String cityName = txtName.getText();
        int cityPopulation = Integer.parseInt(txtPopulation.getText());
        
        flag = MongoCRUD.addCity(cityID, cityName, cityPopulation, productCollection);
        if(flag == false){
            JOptionPane.showMessageDialog(this, "Id not found", "Warning on input data", JOptionPane.WARNING_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Product added succesfully", "Add Product", JOptionPane.INFORMATION_MESSAGE);
            
        }
    }//GEN-LAST:event_btnAddActionPerformed

