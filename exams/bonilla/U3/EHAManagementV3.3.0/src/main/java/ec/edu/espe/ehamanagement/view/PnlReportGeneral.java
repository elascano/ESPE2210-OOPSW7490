    private void refreshTables() {
        SimpleDateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy");
        Date date = new Date();
        lblDate.setText(String.valueOf(dateFormat.format(date)));
        if(!readedOrders.isEmpty()){
            cleanOrdersTable();
            createOrdersRow(readedOrders);
        }else{
            JOptionPane.showMessageDialog(this, "There´s no orders in your agenda", "Empty Agenda", JOptionPane.INFORMATION_MESSAGE);
        }
        if(!readedProducts.isEmpty()){
            cleanProductsTable();
            createProductsRow(readedProducts);
        }else{
            JOptionPane.showMessageDialog(this, "There´s no products in your Inventory", "Empty Inventory", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void createOrdersRow(ArrayList <Order> orders){
        for(Order order:orders){
                String[] information = new String[4];
                information[0]= String.valueOf(order.getId());
                information[1] = order.getClientName();
                information [2] = order.getDeliveryDate();
                information [3] = String.valueOf(order.getIsDelivered());
                ordersTableModel.addRow(information);
            }
    }
    private void createProductsRow(ArrayList <Product> products){
        for(Product product: products){
                String[] information = new String[4];
                information[0]= String.valueOf(product.getId());
                information[1] = product.getName();
                information [2] = String.valueOf(product.getProductionCost());
                information [3] = String.valueOf(product.getQuantity());
                productsTableModel.addRow(information);
            }
    }
    private void cleanOrdersTable() {
        int row = tblTableAgendaReport.getRowCount();
        for (int i = row - 1; i >= 0; i--) {
            ordersTableModel.removeRow(i);
        }
    }
    private void cleanProductsTable() {
        int row = tblTableInventoryReport.getRowCount();
        for (int i = row - 1; i >= 0; i--) {
            productsTableModel.removeRow(i);
        }
    }
