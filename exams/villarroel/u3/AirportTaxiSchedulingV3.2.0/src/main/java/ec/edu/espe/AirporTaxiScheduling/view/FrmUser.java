    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
  
        ArrayList<User> users = new ArrayList<User>();
        String username = txtUser.getText();
        String pass = Password.getText();
        boolean access = false;
        UserdbController userdbController = new UserdbController();
       
        userdbController = UserdbController.connectToDatabase( userdbController);
        UserdbController.loadFromDatabase(users, userdbController.getDatabase(), "Users");

        for (int i = 0; i < users.size(); i++) {
            if (username.equals(users.get(i).getUserName()) && pass.equals(users.get(i).getPassword())) {
                access = true;
            }
        }
        if (access) {
             FrmTarif frmTarif = new FrmTarif();
             TarifSingleton tarifS = TarifSingleton.getInstance();
             float modifyVal = Float.parseFloat(frmTarif.texto);
             tarifS.modifyValue(modifyVal/100);
            JOptionPane.showMessageDialog(null, "Tarifa modificada");
            this.setVisible(false);
            frmTarif.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Error, el usuario o clave son incorrectos");
        }


    }//GEN-LAST:event_btnAcceptActionPerformed

