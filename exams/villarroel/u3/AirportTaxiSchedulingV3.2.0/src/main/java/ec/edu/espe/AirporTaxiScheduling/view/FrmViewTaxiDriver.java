    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
              if (txtCI.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Error, Algunos campos estan vacios, complete los campos");
              } else {
                        if (txtCI.getText().length() == 10) {

                                  if (Validation.IdValidation(txtCI.getText())) {

                                            JOptionPane.showMessageDialog(null, "Numero de cedula ingresado esta incorrecto, vuelva a ingresar");
                                  } else {
                                            TaxiDriver driver = new TaxiDriver(txtCI.getText(), "", 0, "", "", "", "", "", "", "");
                                            driver = TaxiDriverdbController.findDriver(driver);

                                            String[] Datos = new String[10];
                                            Datos[0] = driver.getCI();
                                            Datos[1] = driver.getName();
                                            Datos[2] = String.valueOf(driver.getPhoneNumber());
                                            Datos[3] = driver.getAge();
                                            Datos[4] = driver.getAdress();
                                            Datos[5] = driver.getMail();
                                            Datos[6] = driver.getGender();
                                            Datos[7] = driver.getVehicleMark();
                                            Datos[8] = driver.getVehiclePlate();
                                            Datos[9] = driver.getVehicleYear();

                                            txtCI.setText("");
                                            model.addRow(Datos);
                                            JOptionPane.showMessageDialog(this, "Conductor encontrado exitosamente");
                                  }
                        } else {
                                  JOptionPane.showMessageDialog(null, "La cedula ingresada es incorrecta");
                        }
              }


    }//GEN-LAST:event_btSearchActionPerformed

