    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
              if (txtCIdelete.getText().length() == 10) {
                        if (Validation.IdValidation(txtCIdelete.getText())) {
                                  int option = JOptionPane.showConfirmDialog(this, "Do you want to continue?");
                                  if (option == 0) {
                                            TaxiDriver driver = new TaxiDriver(txtCIdelete.getText(), "", 0, "", "", "", "", "", "", "");
                                            TaxiDriverdbController.deleteDriver(driver);
                                            JOptionPane.showMessageDialog(this, "Driver succesfully removed");
                                            txtCIdelete.setText("");
                                  } else if (option == 1) {
                                            JOptionPane.showMessageDialog(this, "Operation Cancelled");
                                  } else if (option == 2) {
                                            AirportTaxiScheduling backBtm = new AirportTaxiScheduling();
                                            backBtm.setVisible(true);
                                            this.dispose();
                                  }
                        } else {
                                  JOptionPane.showMessageDialog(null, "La cedula ingresada fue invalida");
                        }
              } else {
                        JOptionPane.showMessageDialog(null, "La cedula ingresada es incorrecta");
              }
    }//GEN-LAST:event_btnDeleteActionPerformed

