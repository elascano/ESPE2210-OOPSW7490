    private void txtCIdeleteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCIdeleteKeyTyped
              int validNumber = evt.getKeyChar();
              boolean number = validNumber >= 48 && validNumber <= 57;
              if (!number) {
                        evt.consume();
                        JOptionPane.showMessageDialog(this, number + " only numbers", "Warning on input data", JOptionPane.WARNING_MESSAGE);

              }
              if (txtCIdelete.getText().length() >= 10) {
                        evt.consume();
              }
    }//GEN-LAST:event_txtCIdeleteKeyTyped

