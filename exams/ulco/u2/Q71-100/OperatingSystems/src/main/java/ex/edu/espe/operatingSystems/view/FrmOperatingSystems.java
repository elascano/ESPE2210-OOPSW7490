    private void btnAcceptDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptDeleteActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(this, "Do you want to continue?");
        if (option == 0) {
            OperatingSystem operatingSystem = new OperatingSystem(txtId.getText(), "", "","");
            MongoDB.deleteOperatingSystem(operatingSystem);
            JOptionPane.showMessageDialog(this, " SUCCESSFULLY Delete ");
            txtId.setText("");
        }  
    }//GEN-LAST:event_btnAcceptDeleteActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                OperatingSystem operatingSystem = new OperatingSystem(txtseach.getText(), "", "", "");
                
                operatingSystem = MongoDB.findOperatingSystem(operatingSystem);

        String[] Datos = new String[11];
        Datos[0] = operatingSystem.getId();
        Datos[1] = operatingSystem.getName();
        Datos[2] = operatingSystem.getVersion();
        Datos[3] = operatingSystem.getYear();
       
        
        txtseach.setText("");
         model.addRow(Datos);

    }//GEN-LAST:event_jButton1ActionPerformed

