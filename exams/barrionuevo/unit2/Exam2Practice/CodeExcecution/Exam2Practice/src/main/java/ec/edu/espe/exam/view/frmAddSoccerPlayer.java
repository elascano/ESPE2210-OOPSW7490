    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String uri = "mongodb+srv://domlin:1234@domlin7.gs5if9g.mongodb.net/test";

        try ( MongoClient mongoClient = MongoClients.create(uri)) {
            MongoDatabase database = mongoClient.getDatabase("ExamPractice2");
            try {
                MongoCollection collectionStudents = database.getCollection("SoccerPlayers");
                SoccerPlayer soccerPlayer;

                String name;
                String nationality;
                String age;

                name = String.valueOf(txtName.getText());
                nationality = String.valueOf(txtNationality.getText());
                age = String.valueOf(txtAge.getText());
                soccerPlayer = new SoccerPlayer(name, nationality, age);

                try {
                    InsertOneResult result = collectionStudents.insertOne(new Document()
                            .append("_id", new ObjectId())
                            .append("Name: ", soccerPlayer.getName())
                            .append("Nationality: ", soccerPlayer.getNationality())
                            .append("Age: ", soccerPlayer.getAge())
                    );

                    JOptionPane.showMessageDialog(this, "Success! Inserted document id: " + result.getInsertedId(), "OK", JOptionPane.INFORMATION_MESSAGE);
                    emptyFields();
                } catch (MongoException me) {
                    JOptionPane.showMessageDialog(this, "Unable to insert due to an error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
                    emptyFields();
                }

            } catch (MongoException me) {
                JOptionPane.showMessageDialog(this, "error: " + me, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnAddActionPerformed

