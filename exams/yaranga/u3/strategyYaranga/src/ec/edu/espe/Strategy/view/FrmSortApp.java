    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        StrategyController strategyController = new StrategyController();
        String collectionName = "arrayLeonardo";
        strategyController = StrategyController.connectToDatabase(strategyController);
        SortingContext sortingContext = new SortingContext();
        Strategy strategy = new Strategy();
        String[] numbers = txtAUnorderedNumbers.getText().split(",");
        int[] dataToSort = new int[numbers.length];
        for (int i = 0; i < numbers.length; i++) {
            dataToSort[i] = Integer.parseInt(numbers[i].trim());
        }

        strategy.setUnsorted(dataToSort);
        strategy.setSize(dataToSort.length);
        strategy = sortingContext.sort(strategy);

        StrategyController.createOne(strategy, strategyController.getDatabase(), collectionName);
        txtAOrderedNumbers.setText(Arrays.toString(strategy.getSorted()));
        txtfSortAlgorithm.setText(strategy.getSortAlgorithm());

    }//GEN-LAST:event_btnSortActionPerformed

