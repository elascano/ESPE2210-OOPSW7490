    private void txtAUnorderedNumbersKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAUnorderedNumbersKeyTyped
        char character = evt.getKeyChar();

        if (!Character.isDigit(character) && character != 44 && (character != KeyEvent.VK_BACK_SPACE) && (character != KeyEvent.VK_DELETE) && (character != KeyEvent.VK_SPACE) && (character != KeyEvent.VK_ENTER)) {
            JOptionPane.showMessageDialog(this, character + " Not accepted", "Warning! on the data ingress", JOptionPane.WARNING_MESSAGE);
            evt.consume();
        }
    }//GEN-LAST:event_txtAUnorderedNumbersKeyTyped

