package ec.edu.espe.exam.model;

/**
 *
 * @author kevin
 */
public class Apartment {
    private int id;
    private float cost;
    private int floor;

    public Apartment(int id, float cost, int floor) {
        this.id = id;
        this.cost = cost;
        this.floor = floor;
    }

    
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the cost
     */
    public float getCost() {
        return cost;
    }

    /**
     * @param cost the cost to set
     */
    public void setCost(float cost) {
        this.cost = cost;
    }

    /**
     * @return the floor
     */
    public int getFloor() {
        return floor;
    }

    /**
     * @param floor the floor to set
     */
    public void setFloor(int floor) {
        this.floor = floor;
    }
    
}
