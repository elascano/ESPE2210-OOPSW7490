    private void btmSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmSubmitActionPerformed
        // TODO add your handling code here:
        String uri = "mongodb+srv://JoelRivera:1234@cluster0.nn7qeip.mongodb.net/?retryWrites=true&w=majority";

        try (MongoClient mongoClient = MongoClients.create(uri)) {

            MongoDatabase database = mongoClient.getDatabase("UrbanizationTreasurySystem");

            try {
                //Connect with server
                Bson command = new BsonDocument("ping", new BsonInt64(1));
                org.bson.Document commandResult = database.runCommand(command);
                System.out.println("Connected successfully to server.");

                MongoCollection<Document> collection = database.getCollection("plates");
                
                String[] info = new String[3];
                
                plate.setId(Long.parseLong(txtId.getText()));
                plate.setName(txtName.getText());
                plate.setPlateCode(txtPlateCode.getText());
                
                Document inspection = new Document("_id", new ObjectId())
                        .append("id", plate.getId())
                        .append("month", plate.getName())
                        .append("plate code", plate.getPlateCode());
                
                info[0]=txtId.getText();
                info[1]=txtName.getText();
                info[2]=txtPlateCode.getText();
                
                model.addRow(info);
                
                
                collection.insertOne(inspection);

            } catch (MongoException me) {
                System.err.println("An error occurred while attempting to connect: " + me);
            }
        }

    }//GEN-LAST:event_btmSubmitActionPerformed

