    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        if (!(tfId.getText().equals("")))
        {
            int id = Integer.parseInt(tfId.getText());
            StudentController studentController = new StudentController();

            Student student = studentController.find(id);
            if (student.getId() != 0)
            {
                btnSave.setEnabled(true);
                tfUnit1.setText(String.valueOf(student.getGrades()[0]));
                tfUnit2.setText(String.valueOf(student.getGrades()[1]));
                tfUnit3.setText(String.valueOf(student.getGrades()[2]));
                tfGPA.setText(String.valueOf(student.getGPA()));
            } else
            {
                btnSave.setEnabled(true);
                emptyFields();
            }

            labelStudent.setText(student.getName());

        } else {
            JOptionPane.showMessageDialog(this, "First enter an ID");
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StudentController student = new StudentController();
        float grades[] = {Float.parseFloat(tfUnit1.getText()), Float.parseFloat(tfUnit2.getText()), Float.parseFloat(tfUnit3.getText())};
        float GPA = student.computeAverage(grades);
        
        tfGPA.setText(String.valueOf(GPA));
        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to save it?");
        if(option == 0){
            emptyFields();
            btnSave.setEnabled(false);
        }
        if(option == 1){
            emptyFields();
            btnSave.setEnabled(false);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

