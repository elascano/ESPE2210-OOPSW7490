    private void btmFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmFindActionPerformed
        StudentCtl studentCtl = new StudentCtl();
        Student student=null;
        int id = Integer.valueOf(txtId.getText());
        student = studentCtl.find(id);
        
        if(student.getId()!=0){
        btmSave.setEnabled(true);
        txtUnit1Grade.setText(String.valueOf(student.getGrades()[0]));
        txtUnit2Grade.setText(String.valueOf(student.getGrades()[1]));
        txtUnit3Grade.setText(String.valueOf(student.getGrades()[2]));
        txtGPA.setText(String.valueOf(student.getGPA()));
        }else{
            btmSave.setEnabled(false);
            emptyFields();
        }
        
        lblStudentName.setText(student.getName());
        
        
        
    }//GEN-LAST:event_btmFindActionPerformed

    private void btmSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmSaveActionPerformed
        StudentCtl studentCtl = new StudentCtl();
        float grades[] = {Float.parseFloat(txtUnit1Grade.getText()),Float.parseFloat(txtUnit2Grade.getText()),Float.parseFloat(txtUnit3Grade.getText())};
        float GPA = studentCtl.computeAverage(grades);
        
        txtGPA.setText(String.valueOf(GPA));
        int desition = JOptionPane.showConfirmDialog(this,"Are you sure you want to save these grades");
        //code if 0,1,2
        if(desition==0){
            emptyFields();
            btmSave.setEnabled(false);
        }
        if(desition==1){
            emptyFields();
            btmSave.setEnabled(false);
        }
        if(desition==2){
            
        }
    }//GEN-LAST:event_btmSaveActionPerformed

    
