    private void btnFindStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindStudentActionPerformed
        StudentController studentController = new StudentController();
        Student student;
        int id = Integer.parseInt(txtId.getText());
        student = studentController.find(id);

        lblStudentName.setText(student.getName());

        if (student.getId() != 0) {
            btnGrade.setEnabled(true);
            txtUnit1.setText(String.valueOf(student.getGrades()[0]));
            txtUnit2.setText(String.valueOf(student.getGrades()[1]));
            txtUnit3.setText(String.valueOf(student.getGrades()[2]));

            float gradeU1 = Float.parseFloat(txtUnit1.getText());
            float gradeU2 = Float.parseFloat(txtUnit2.getText());
            float gradeU3 = Float.parseFloat(txtUnit3.getText());
            float grades[] = {gradeU1, gradeU2, gradeU3};

            float gpa = studentController.computeAverage(grades);
            lblGpa.setText(String.valueOf(gpa));
        } else {
            emptyFields();
            btnGrade.setEnabled(false);
        }
    }//GEN-LAST:event_btnFindStudentActionPerformed

    private void btnGradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGradeActionPerformed
        StudentController studentController = new StudentController();
        float gradeU1 = Float.parseFloat(txtUnit1.getText());
        float gradeU2 = Float.parseFloat(txtUnit2.getText());
        float gradeU3 = Float.parseFloat(txtUnit3.getText());
        float grades[] = {gradeU1, gradeU2, gradeU3};

        float gpa = studentController.computeAverage(grades);
        lblGpa.setText(String.valueOf(gpa));
        int desicion = JOptionPane.showConfirmDialog(this, "Are you sure you wantto save these grades");

        if (desicion == 0) {
            emptyFields();
            
            btnGrade.setEnabled(false);
        }

        if (desicion == 1) {
            emptyFields();
            btnGrade.setEnabled(false);
        }
    }//GEN-LAST:event_btnGradeActionPerformed

    private void emptyFields() {
        lblGpa.setText("");
        txtUnit1.setText("");
        txtUnit2.setText("");
        txtUnit3.setText("");
    }

