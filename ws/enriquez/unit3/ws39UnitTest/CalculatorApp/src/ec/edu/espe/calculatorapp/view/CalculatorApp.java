        // TODO add your handling code here:
        float sum;
        float addend1;
        float addend2;
        txtTotalNumber.setText("");
        addend1 = Float.parseFloat(txtNumber1.getText());
        addend2 = Float.parseFloat(txtNumber2.getText());
