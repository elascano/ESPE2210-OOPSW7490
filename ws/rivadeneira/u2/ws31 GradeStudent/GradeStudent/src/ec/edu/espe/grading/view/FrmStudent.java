    private void btmFindStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmFindStudentActionPerformed
        StudentController studentController = new StudentController();
        Student student = null;
        int id = Integer.valueOf(txtId.getText());
        student = studentController.find(id);
        if(student.getId() !=0){
            btnSave.setEnabled(true);
            txtUnit1.setText(String.valueOf(student.getGrades()[0]));
            txtUnit2.setText(String.valueOf(student.getGrades()[1]));
            txtUnit3.setText(String.valueOf(student.getGrades()[2]));
            txtGPA.setText(String.valueOf(student.getGPA()));
            
        }else{
            btnSave.setEnabled(false);
            emptyFields();
        }
        
        lblStudentName.setText(student.getName());
    }//GEN-LAST:event_btmFindStudentActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       StudentController studentController = new StudentController();
       float grades[] = {Float.parseFloat(txtUnit1.getText()), Float.parseFloat(txtUnit2.getText()), Float.parseFloat(txtUnit3.getText())};
       float GPA = studentController.computeAverage(grades);
       txtGPA.setText(String.valueOf(GPA));
       int desicion = JOptionPane.showConfirmDialog(this, "Are you sure to want save these grades?");
       
        if(desicion == 0){
            emptyFields();
            btnSave.setEnabled(false);  
        }
         if (desicion == 1){
            emptyFields();
            btnSave.setEnabled(false);  
        }
         if (desicion == 2){
        }
    }//GEN-LAST:event_btnSaveActionPerformed

