    public void toList() throws UnknownHostException{
        tabla = db.getCollection("User");
        cursor = tabla.find();
        
        String [] columnNames = {"Name","Telephone","Direction"};
        DefaultTableModel model = new DefaultTableModel(columnNames,0);
        
        while(cursor.hasNext()){
            DBObject obj = cursor.next();
            String name = (String)obj.get("Name");
            String telephone = (String)obj.get("Telephone");
            String direction = (String)obj.get("Direction");
            model.addRow(new Object[]{name, telephone, direction});
            
        }
        //table.setModel(model);
    }
    
