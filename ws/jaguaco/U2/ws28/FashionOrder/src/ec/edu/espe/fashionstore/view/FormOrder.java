    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
        // TODO add your handling code here:  
        document.put("Name", txtname.getText());
        cursor = tabla.find(document);
        
        while(cursor.hasNext()){
            DBObject obj = cursor.next();
            String name = (String)obj.get("Name");
            String telephone = (String)obj.get("Telephone");
            String neighborhood = (String)obj.get("Neighborhood");
            
            txtname.setText(name);
            tlftext.setText(telephone);
            directiontxt.setText(neighborhood);
            System.out.println(name + telephone + neighborhood);
        }
    }//GEN-LAST:event_btnfindActionPerformed

