    private void btnmodifiedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifiedActionPerformed
        // TODO add your handling code here:
        if(txtname.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Complete the name");
        }else
        {
          document.put("Name", txtname.getText());
          BasicDBObject documentnew = new BasicDBObject();
          documentnew.put("Name","'"+txtname.getText() + "'" );
          documentnew.put("Telephone", tlftext.getText());
          documentnew.put("Direction", directiontxt.getText());
          documentnew.put("Year", cmbYear.getSelectedItem());
          documentnew.put("Month", cmbMonth.getSelectedItem());
          documentnew.put("Day", cmbDay.getSelectedItem());
          tabla.findAndModify(document, documentnew);
        }
    }//GEN-LAST:event_btnmodifiedActionPerformed

