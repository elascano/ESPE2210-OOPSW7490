    private void btnFindStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindStudentActionPerformed
        StudentController studentController = new StudentController();
        Student student = null;
        int id = Integer.valueOf(txtId.getText());
        student = studentController.find(id);
        if (student.getId() != 0) {
            btnSave.setEnabled(true);
            txtGradeUnit1.setText(String.valueOf(student.getGrades()[0]));
            txtGradeUnit2.setText(String.valueOf(student.getGrades()[1]));
            txtGradeUnit3.setText(String.valueOf(student.getGrades()[2]));
        } else {
            btnSave.setEnabled(false);
            emptyFields();
        }
        lblName.setText(student.getName());
    }//GEN-LAST:event_btnFindStudentActionPerformed

