    private void btnMultiplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMultiplyActionPerformed
        float multiplicand = Float.parseFloat(txtMultiplicand.getText());
        float multiplier = Float.parseFloat(txtMultiplier.getText());
        
        BasicOperation operation = new BasicOperation();
        
        float product = operation.multiply(multiplicand, multiplier);
        
        lblProduct.setText(String.valueOf(product));
    }//GEN-LAST:event_btnMultiplyActionPerformed

