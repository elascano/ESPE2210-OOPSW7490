    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        StudentController studentController = new StudentController();
        Student student;
        int id = Integer.parseInt(txtId.getText());
        student = studentController.find(id);
        if (student.getId()!=0){
            btnSave.setEnabled(true);
            txtUnit1.setText(String.valueOf(student.getGrades()[0]));
            txtUnit2.setText(String.valueOf(student.getGrades()[1]));
            txtUnit3.setText(String.valueOf(student.getGrades()[2]));
            txtGPA.setText(String.valueOf(student.getGPA()));
        }else{
            btnSave.setEnabled(false);
            emptyFields();
        }
        lblStudentName.setText(student.getName()); 
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        StudentController studetnController = new StudentController();
        float grade1 = Float.parseFloat(txtUnit1.getText());
        float grade2 = Float.parseFloat(txtUnit2.getText());
        float grade3 = Float.parseFloat(txtUnit3.getText());
        float grades [] ={grade1, grade2, grade3};
        float GPA = studetnController.computeAverage(grades);
        txtGPA.setText(String.valueOf(GPA));
        int desicion = JOptionPane.showConfirmDialog(this, "Are you sure you want to update grades?"); 
        switch(desicion){
            case 0->{
                emptyFields();
                btnSave.setEnabled(false);
            }
            case 1 ->{
                
            }
            case 2->{
                emptyFields();
                btnSave.setEnabled(false);
            }
                
        }
    }//GEN-LAST:event_btnSaveActionPerformed

