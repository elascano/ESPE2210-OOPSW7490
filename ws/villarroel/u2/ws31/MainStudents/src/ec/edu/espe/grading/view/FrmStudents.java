    private void btmFSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmFSActionPerformed
        StudentController studentController = new StudentController();
        Student student = null;
        int id = Integer.valueOf(tfid.getText());
        student = studentController.find(id);
        if(student.getId()!=0){
            btmSave.setEnabled(true);
            tfid1.setText(String.valueOf(student.getGrades()[0]));
            tfid2.setText(String.valueOf(student.getGrades()[1]));
            tfid3.setText(String.valueOf(student.getGrades()[2]));
            gpaText.setText(String.valueOf(student.getGPA()));
        }else{
            emptyText();
        }
        
        lbSN.setText(student.getName());
    }//GEN-LAST:event_btmFSActionPerformed

    private void btmSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmSaveActionPerformed
        StudentController studentController = new StudentController();
        float grades[] = {Float.parseFloat(tfid1.getText()),Float.parseFloat(tfid2.getText()),Float.parseFloat(tfid3.getText()) };
        float GPA = studentController.computeAverage(grades);
        gpaText.setText(String.valueOf(GPA));
        JOptionPane.showConfirmDialog(this,"Are you sure you want Save these Information?");
    }//GEN-LAST:event_btmSaveActionPerformed

    private void emptyText(){
        tfid1.setText("");
        tfid2.setText("");
        tfid3.setText("");
        gpaText.setText("");
    }
